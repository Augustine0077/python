class Circle:
    def __init__(self,colour,radius,is_filled):
        self.colour = colour 
        self.radius = radius
        self.is_filled = is_filled
class Square:
    def __init__(self,colour,width,is_filled):
        self.colour = colour 
        self.width = width
        self.is_filled = is_filled
class Triangle:
    def __init__(self,colour,width,height,is_filled):
        self.colour = colour 
        self.width = width
        self.height = height
        self.is_filled = is_filled